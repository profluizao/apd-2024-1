public class Calculadora {
    public int Adicao(int x, int y){
        return x + y;
    }

    public int Subtracao(int x, int y){
        return x - y;
    }

    public int Multiplicao(int x, int y){
        return x * y;
    }

    public int Divisao(int x, int y){
        return x / y;
    }

}
